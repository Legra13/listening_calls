# Собираем items_raw по criterion_id
items_raw = []
for ctext, val, comment in EVAL_DATA:
    cid = crit_ids.get(ctext)
    if cid is None:
        print(f"  WARN: критерий не найден: {ctext!r}")
        continue
    items_raw.append((cid, val, comment))

# Перезагрузить чек-лист с joinedload для расчёта баллов
from sqlalchemy.orm import joinedload
cl_full = (
    db.query(Checklist)
    .options(joinedload(Checklist.blocks).joinedload(Block.criteria))
    .filter(Checklist.id == cl.id)
    .first()
)

total_score, block_scores = calculate_scores(items_raw, cl_full)
print(f"Итоговый балл: {total_score:.1f}%")
for block in cl_full.blocks:
    bs = block_scores.get(block.id, 0)
    print(f"  {block.name}: {bs:.0f}%")

