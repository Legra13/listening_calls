db = SessionLocal()

cl = (
    db.query(Checklist)
    .options(joinedload(Checklist.blocks).joinedload(Block.criteria))
    .filter(Checklist.name == "ОПП")
    .first()
)

if not cl:
    print("Чек-лист ОПП не найден")
    db.close()
    sys.exit(1)

updated = 0
not_found = []

for block in cl.blocks:
    for crit in block.criteria:
        desc = DESCRIPTIONS.get(crit.text)
        if desc:
            crit.description = desc
            updated += 1
        else:
            not_found.append(crit.text)

db.commit()
db.close()

print(f"Обновлено критериев: {updated}")
if not_found:
    print(f"Не найдено соответствий ({len(not_found)}):")
