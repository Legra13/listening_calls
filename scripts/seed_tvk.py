def main():
    create_tables()
    db = SessionLocal()

    existing = db.query(Checklist).filter(Checklist.name == CHECKLIST_NAME).first()
    if existing:
        answer = input(f"Чек-лист «{CHECKLIST_NAME}» уже существует (id={existing.id}). Пересоздать? [y/N]: ").strip().lower()
        if answer != "y":
            print("Отменено")
            db.close()
            return
        db.delete(existing)
        db.commit()

    cl = Checklist(name=CHECKLIST_NAME, description=CHECKLIST_DESC)
    db.add(cl)
    db.commit()
    db.refresh(cl)

    for block_idx, (key, display, weight, criteria) in enumerate(BLOCKS):
        block = Block(
            checklist_id=cl.id,
            name=key,
            display_name=display,
            weight=weight,
            order_index=block_idx,
        )
        db.add(block)
        db.commit()
        db.refresh(block)

        for crit_idx, (text, crit_weight) in enumerate(criteria):
            crit = Criterion(
                block_id=block.id,
                text=text,
                weight=crit_weight,
                order_index=crit_idx,
            )
            db.add(crit)

    db.commit()
    db.close()

    blocks_count = len(BLOCKS)
    criteria_count = sum(len(c) for _, _, _, c in BLOCKS)
    print(f"Чек-лист «{CHECKLIST_NAME}» загружен: {blocks_count} блоков, {criteria_count} критериев")


if __name__ == "__main__":
    main()
