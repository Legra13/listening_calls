def main():
    db = SessionLocal()
    try:
        # Проверяем, нет ли уже такого чек-листа
        existing = db.query(Checklist).filter(Checklist.name == CHECKLIST_NAME).first()
        if existing:
            print(f"Чек-лист «{CHECKLIST_NAME}» уже существует (id={existing.id}). Пропускаем.")
            return existing.id

        # Создаём чек-лист
        cl = Checklist(
            name=CHECKLIST_NAME,
            description="Чек-лист оценки звонков отдела продаж",
            status="active",
            is_active=True,
            calculation="weighted",
            autofail_enabled=False,
        )
        db.add(cl)
        db.flush()

        for block_idx, block_data in enumerate(BLOCKS_DATA):
            block = Block(
                checklist_id=cl.id,
                name=block_data["name"],
                display_name=block_data["name"],
                weight=block_data["weight"],
                order_index=block_idx,
            )
            db.add(block)
            db.flush()

            for crit_idx, (text, description, weight) in enumerate(block_data["criteria"]):
                crit = Criterion(
                    block_id=block.id,
                    text=text,
                    description=description,
                    weight=weight,
                    is_autofail=False,
                    order_index=crit_idx,
                    score_type="binary",
                    score_max=5,
                )
                db.add(crit)

        db.commit()
        db.refresh(cl)
        print(f"Чек-лист «{CHECKLIST_NAME}» создан. ID = {cl.id}")
        print(f"Блоков: {len(BLOCKS_DATA)}")
        total_criteria = sum(len(b["criteria"]) for b in BLOCKS_DATA)
        print(f"Критериев: {total_criteria}")
        return cl.id
    finally:
        db.close()


if __name__ == "__main__":
    main()
